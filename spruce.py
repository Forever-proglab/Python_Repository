# Начнем с функции для отображения ёлочки
def draw_tree(height):
    for i in range(1, height + 1):
        # Пространство перед звездочками
        spaces = ' ' * (height - i)
        # Звездочки для текущего уровня
        stars = '*' * (2 * i - 1)
        print(spaces + stars)
    # Рисование ствола ёлки
    print(' ' * (i - 2),'^')

# Запрашиваем у пользователя высоту ёлочки
tree_height = int(input("Введите высоту ёлочки: "))

# Вызываем функцию для отображения ёлочки
draw_tree(tree_height)
